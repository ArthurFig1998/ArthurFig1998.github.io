


function optionOne() {

    var optionOne = document.getElementById('option-one');
    var optionTwo = document.getElementById('option-two');
    var optionThree = document.getElementById('option-three');
    var optionOneContent = document.getElementById('option-one-content');
    var optionTwoContent = document.getElementById('option-two-content');
    var optionThreeContent = document.getElementById('option-three-content');
    var activity = document.getElementById('activity');
    var invite = document.getElementById('invite');

    activity.style.display = "flex";
    invite.style.display = "block";

    optionOne.style.backgroundColor = "#2677B9";
    optionOne.style.color = "white";
    
    optionTwo.style.backgroundColor = "#EAEAEA";
    optionTwo.style.color = "#707070";

    optionThree.style.backgroundColor = "#EAEAEA";
    optionThree.style.color = "#707070";

    optionOneContent.style.display = "flex";
    optionTwoContent.style.display = "none";
    optionThreeContent.style.display = "none";
}

